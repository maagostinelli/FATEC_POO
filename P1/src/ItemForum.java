public class ItemForum {
    private int id;
    private String autor;
    private String texto;

    public void setId(int id) {
        this.id = id;
    }
    public int getId() {
        return id;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }
    public String getAutor() {
        return autor;
    }

    public void setTexto(String texto) {
        this.texto = texto;
    }
    public String getTexto() {
        return texto;
    }
}